    private void jButtonCariMakananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariMakananActionPerformed
        // TODO add your handling code here:
        jPanelMakan1.setVisible(false);
        jPanelMakan2.setVisible(false);
        jPanelMakan3.setVisible(false);
        jPanelMakan4.setVisible(false);
        jPanelMakan5.setVisible(false);
        
        String cek = pencarianMakanan.getText();
        cek = cek.toLowerCase();
        
        String[] dicek = {namaMakan1.getText(), namaMakan2.getText(), namaMakan3.getText(), namaMakan4.getText(), namaMakan5.getText()};
        dicek[0] = dicek[0].toLowerCase();
        dicek[1] = dicek[1].toLowerCase();
        dicek[2] = dicek[2].toLowerCase();
        dicek[3] = dicek[3].toLowerCase();
        dicek[4] = dicek[4].toLowerCase();
        
        int jumlahVisible = 0;

        for (int i = 0; i < dicek.length; i++) {
            int pengecekan[] = new int[dicek.length];
            pengecekan[i] = stringMatch(dicek[i], cek);
            if (pengecekan[0] == 1) {
                jPanelMakan1.setVisible(true);
                jumlahVisible++;
            } else if (pengecekan[1] == 1) {
                jPanelMakan2.setVisible(true);
                jumlahVisible++;
            } else if (pengecekan[2] == 1) {
                jPanelMakan3.setVisible(true);
                jumlahVisible++;
            } else if (pengecekan[3] == 1) {
                jPanelMakan4.setVisible(true);
                jumlahVisible++;
            } else if (pengecekan[4] == 1) {
                jPanelMakan5.setVisible(true);
                jumlahVisible++;
            }
        }

        int height = setContainer1Height(jumlahVisible);
        jPanelContainerMakan.setPreferredSize(new Dimension(490, height));
        jPanelContainerMakan.revalidate();
    }//GEN-LAST:event_jButtonCariMakananActionPerformed

