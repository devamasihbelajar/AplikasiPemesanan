    // Method untuk melakukan pencarian
    public static int stringMatch(String text, String pattern) 
    {

        int len_t = text.length(); 
        int len_p = pattern.length(); 

        int k = 0, i = 0, j = 0; 

        // loop to find out the position Of searched pattern 
        for (i = 0; i <= (len_t - len_p); i++) { 
            
            for (j = 0; j < len_p; j++) 
            { 
                if (text.charAt(i + j) != pattern.charAt(j)) 
                    break; 
            } 
            
            if (j == len_p) 
            { 
                k++; 
            } 
        } 
        
        if (k == 0) 
            return 0;
        else
            return 1;
    }
    
    public void pesananSelesai() {
        String selectedOption = (String) comboPesan.getSelectedItem();
        
        int noMeja = Koneksi.getNoMejaPelanggan();
        
        if ("Dine In".equals(selectedOption)) {
            JOptionPane.showMessageDialog(null, "Pesanan berhasil dilakukan, silahkan menunggu pada meja " + noMeja, "Pesanan berhasil", JOptionPane.INFORMATION_MESSAGE);
            saveHarga();
        } else if ("Takeaway".equals(selectedOption)) {
            // Jika memilih Takeaway
            JOptionPane.showMessageDialog(null, "Pesanan berhasil dilakukan, Silahkan menunggu untuk nama anda dipanggil", "Pesanan Berhasil", JOptionPane.INFORMATION_MESSAGE);
            saveHarga();
        }
    }
    
