    public void saveHarga() {
        int harga1 = parseAndClean(hargaMie.getText());
        int harga2 = parseAndClean(hargaKuah.getText());
        int harga3 = parseAndClean(hargaRice.getText());
        int harga4 = parseAndClean(hargaSayap.getText());
        int harga5 = parseAndClean(hargaSpaghetti.getText());
        int harga6 = parseAndClean(hargaMatcha.getText());
        int harga7 = parseAndClean(hargaCapu.getText());
        int harga8 = parseAndClean(hargaTea.getText());
        int harga9 = parseAndClean(hargaOrange.getText());

        int hargatotal = (harga1 + harga2 + harga3 + harga4 + harga5 + harga6 + harga7 + harga8 + harga9);
        hargatotal = (hargatotal/100);
        Koneksi.updateHargaTotal(hargatotal);
    }
    
    private int parseAndClean(String input) {
        // Remove non-numeric characters from the input string
        String cleanedInput = input.replaceAll("[^0-9]", "");

        // Parse the cleaned string into an integer
        try {
            return Integer.parseInt(cleanedInput);
        } catch (NumberFormatException e) {
            // Handle the exception, for example, show an error message to the user
            e.printStackTrace();  // You might want to log the exception or handle it in a more user-friendly way
            return 0;  // Or any default value that makes sense in your application
        }
    }
    
